# Trainer patché : perte pondérée + label smoothing
class WeightedTrainer(Trainer):
    def __init__(self, *args, class_weights=None, label_smoothing=0.0, **kwargs):
        super().__init__(*args, **kwargs)
        self.class_weights = class_weights
        self.label_smoothing = float(label_smoothing)

    # Accepte les kwargs potentiels (ex: num_items_in_batch)
    def compute_loss(self, model, inputs, return_outputs=False, **kwargs):
        labels = inputs.pop("labels")
        outputs = model(**inputs)
        logits = outputs.logits

        if self.label_smoothing > 0:
            num_labels = logits.size(-1)
            with torch.no_grad():
                true_dist = torch.zeros_like(logits)
                true_dist.fill_(self.label_smoothing / (num_labels - 1))
                true_dist.scatter_(1, labels.unsqueeze(1), 1.0 - self.label_smoothing)
            log_probs = torch.log_softmax(logits, dim=-1)
            weights = self.class_weights.to(logits.device)
            per_ex_weight = weights[labels]
            loss = -(per_ex_weight * (true_dist * log_probs).sum(dim=1)).mean()
        else:
            loss_fct = nn.CrossEntropyLoss(weight=self.class_weights.to(logits.device))
            loss = loss_fct(logits, labels)

        return (loss, outputs) if return_outputs else loss

# TrainingArguments : moderne -> fallback legacy
def make_args_compat(num_epochs=2, lr=2e-5):
    common = dict(
        output_dir="out_roberta_weighted",
        learning_rate=lr,
        per_device_train_batch_size=16,
        per_device_eval_batch_size=64,
        num_train_epochs=num_epochs,
        weight_decay=0.01,
        logging_steps=100,
        seed=SEED,
    )
