def align_len(y_true_bin, preds_dict):
    """Alerte et tronque si tailles différentes."""
    n = len(y_true_bin)
    aligned = {}
    for name, preds in preds_dict.items():
        if len(preds) != n:
            print(f"[WARN] {name}: {len(preds)} prédictions vs {n} vérités. Tronquage à {n}.")
            aligned[name] = preds[:n]
        else:
            aligned[name] = preds
    return aligned

